-- Metricas
--1. ¿Cuál es el volumen de transacciones por tipo (retiro, depósito, transferencia)?

SELECT 
STR_TIPO_TRANSACCION, 
COUNT(NUM_DIM_TRANSACCION_SK) AS cantidad_trx,
SUM(STR_MONTO) AS total_monto
FROM FCT_TRANSACCIONES
GROUP BY 
STR_TIPO_TRANSACCION;

--2. ¿Cómo varían las transacciones por canal (cajero, app, sucursal) y por periodo?

SELECT  
DC.STR_NOMBRE_CANAL,
DT.NUM_MES,
DT.NUM_ANIO,
COUNT(FT.NUM_DIM_TRANSACCION_SK) AS cantidad_trx
FROM FCT_TRANSACCIONES FT
JOIN DIM_CANAL DC ON FT.NUM_DIM_CANAL_SK = DC.NUM_DIM_CANAL_SK
JOIN DIM_TIEMPO DT ON FT.NUM_DIM_FECHA_SK = DT.NUM_DIM_FECHA_SK
GROUP BY 
DC.STR_NOMBRE_CANAL, DT.NUM_MES, DT.NUM_ANIO
ORDER BY 
DT.NUM_ANIO, DT.NUM_MES, DC.STR_NOMBRE_CANAL;



-- 3. ¿Qué clientes realizan más operaciones y en qué productos financieros?
SELECT 
DC.STR_CLIENTE_ID_NK,
DC.STR_NOMBRE,
DC.STR_SEGMENTO,
COUNT(FT.NUM_DIM_TRANSACCION_SK) AS Num_operaciones,
SUM(STR_MONTO) AS total_monto
FROM FCT_TRANSACCIONES FT
JOIN DIM_CLIENTE DC ON FT.NUM_DIM_CLIENTE_SK = DC.NUM_DIM_CLIENTE_SK
GROUP BY 
DC.STR_CLIENTE_ID_NK, DC.STR_NOMBRE, DC.STR_SEGMENTO;


