-- DDL de las dimensiones y hechos
CREATE TABLE DIM_CLIENTE (
    NUM_DIM_CLIENTE_SK NUMBER PRIMARY KEY,
    STR_CLIENTE_ID_NK VARCHAR2(50),
    STR_NOMBRE VARCHAR2(100),
    NUM_EDAD NUMBER,
    STR_SEGMENTO VARCHAR2(50),
    STR_CIUDAD VARCHAR2(50),
    STR_REGION VARCHAR2(50)
);

CREATE TABLE DIM_PRODUCTO (
    NUM_DIM_PRODUCTO_SK NUMBER PRIMARY KEY,
    STR_PRODUCTO_ID_NK VARCHAR2(50),
    STR_NOMBRE_PRODUCTO VARCHAR2(100),
    STR_TIPO_PRODUCTO VARCHAR2(50),
    NUM_TASA_INTERES NUMBER
);

CREATE TABLE DIM_TIEMPO (
    NUM_DIM_FECHA_SK NUMBER PRIMARY KEY,
    DTM_FECHA_NK DATE,
    NUM_DIA NUMBER,
    NUM_MES NUMBER,
    NUM_TRIMESTRE NUMBER,
    NUM_ANIO NUMBER
);

CREATE TABLE DIM_CANAL (
    NUM_DIM_CANAL_SK NUMBER PRIMARY KEY,
    STR_CANAL_ID_NK VARCHAR2(50),
    STR_NOMBRE_CANAL VARCHAR2(50)
);

CREATE TABLE FCT_TRANSACCIONES (
    NUM_DIM_TRANSACCION_SK NUMBER PRIMARY KEY,
    STR_TRANSACCION_ID_NK VARCHAR2(50),
    NUM_DIM_FECHA_SK NUMBER,
    NUM_DIM_CLIENTE_SK NUMBER,
    NUM_DIM_PRODUCTO_SK NUMBER,
    NUM_DIM_CANAL_SK NUMBER,
    STR_MONTO VARCHAR2(50),
    STR_TIPO_TRANSACCION VARCHAR2(50),
    FOREIGN KEY (NUM_DIM_FECHA_SK) REFERENCES DIM_TIEMPO(NUM_DIM_FECHA_SK),
    FOREIGN KEY (NUM_DIM_CLIENTE_SK) REFERENCES DIM_CLIENTE(NUM_DIM_CLIENTE_SK),
    FOREIGN KEY (NUM_DIM_PRODUCTO_SK) REFERENCES DIM_PRODUCTO(NUM_DIM_PRODUCTO_SK),
    FOREIGN KEY (NUM_DIM_CANAL_SK) REFERENCES DIM_CANAL(NUM_DIM_CANAL_SK)
);
DROP TABLE FCT_TRANSACCIONES;

